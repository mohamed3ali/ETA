const DICTIONARIES: Record<Locale, Dict> = { ar, en };

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) =>
    vars[k] === undefined || vars[k] === null ? `{${k}}` : String(vars[k]),
  );
}

interface I18nContextValue {
  locale: Locale;
  dir: 'ltr' | 'rtl';
  setLocale: (l: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(DEFAULT_LOCALE);

  useEffect(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY) as Locale | null;
      if (saved === 'ar' || saved === 'en') setLocaleState(saved);
    } catch {}
  }, []);

  useEffect(() => {
    const dir = locale === 'ar' ? 'rtl' : 'ltr';
    if (typeof document !== 'undefined') {
      document.documentElement.lang = locale;
      document.documentElement.dir = dir;
    }
  }, [locale]);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {}
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = DICTIONARIES[locale] ?? en;
      const value = dict[key] ?? en[key] ?? key;
      return interpolate(value, vars);
    },
    [locale],
  );

  const value = useMemo<I18nContextValue>(
    () => ({ locale, dir: locale === 'ar' ? 'rtl' : 'ltr', setLocale, t }),
    [locale, setLocale, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within LanguageProvider');
  return ctx;
}

export function useT() {
  return useI18n().t;
}

export function useLocale() {
  return useI18n().locale;
}

export const intlLocale = (locale: Locale) => (locale === 'ar' ? 'ar-EG' : 'en-EG');

export function useFormatters() {
  const { locale } = useI18n();
  const intl = intlLocale(locale);
  const formatCurrency = useCallback(
    (value: number | string | null | undefined, currency = 'EGP') => {
      const n = Number(value ?? 0);
      return new Intl.NumberFormat(intl, {
        style: 'currency',
        currency,
        maximumFractionDigits: 2,
      }).format(n);
    },
    [intl],
  );
  const formatDate = useCallback(
    (d: string | Date | null | undefined) => {
      if (!d) return '—';
      const date = typeof d === 'string' ? new Date(d) : d;
      return new Intl.DateTimeFormat(intl, {
        day: '2-digit',
        month: 'short',
        year: 'numeric',
      }).format(date);
    },
    [intl],
  );
  return { formatCurrency, formatDate, locale };
}

export const STATUS_KEYS = [
  'draft',
  'submitted',
  'accepted',
  'rejected',
  'paid',
  'overdue',
  'cancelled',
] as const;
export type StatusKey = (typeof STATUS_KEYS)[number];

export const STATUS_BADGE: Record<
  StatusKey,
  'default' | 'secondary' | 'destructive' | 'success' | 'warning' | 'outline' | 'muted'
> = {
  draft: 'muted',
  submitted: 'default',
  accepted: 'success',
  rejected: 'destructive',
  paid: 'success',
  overdue: 'warning',
  cancelled: 'muted',
};

export function useStatusLabel() {
  const t = useT();
  return useCallback((s: StatusKey) => t(`status.${s}`), [t]);
}
